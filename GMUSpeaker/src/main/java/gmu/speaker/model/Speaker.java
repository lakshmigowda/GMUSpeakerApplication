package gmu.speaker.model;

public class Speaker {
	private String id;
	private String name;
	private String workPhone;
	private String homePhone;
	private String cellPhone;
	private String email;
	private String masonEmployee;
	private String retired;
	private String masonAlumnus;
	private String biography;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getWorkPhone() {
		return workPhone;
	}

	public void setWorkPhone(String workPhone) {
		this.workPhone = workPhone;
	}

	public String getHomePhone() {
		return homePhone;
	}

	public void setHomePhone(String homePhone) {
		this.homePhone = homePhone;
	}

	public String getCellPhone() {
		return cellPhone;
	}

	public void setCellPhone(String cellPhone) {
		this.cellPhone = cellPhone;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getMasonEmployee() {
		return masonEmployee;
	}

	public void setMasonEmployee(String masonEmployee) {
		this.masonEmployee = masonEmployee;
	}

	public String getRetired() {
		return retired;
	}

	public void setRetired(String retired) {
		this.retired = retired;
	}

	public String getMasonAlumnus() {
		return masonAlumnus;
	}

	public void setMasonAlumnus(String masonAlumnus) {
		this.masonAlumnus = masonAlumnus;
	}

	public String getBiography() {
		return biography;
	}

	public void setBiography(String biography) {
		this.biography = biography;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

}
